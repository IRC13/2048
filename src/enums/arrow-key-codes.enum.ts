/* TS enums allow us to specify only 1st number (i.e 'Left = 37')
 * and rest values will be automatically calculated by increment.
 * However, I prefer this syntax as it's more verbose
 * and easier understandable for less experienced engineers
 */
export enum ArrowKeyCodes {
  Left = 37,
  Up = 38,
  Right = 39,
  Down = 40,
}
